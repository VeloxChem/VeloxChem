def _finite_difference_energy_gradient(driver, template_molecule, coordinates, step):
    fd = np.zeros_like(coordinates, dtype=np.float64)
    for atom_idx in range(coordinates.shape[0]):
        for axis_idx in range(3):
            coords_p = np.array(coordinates, copy=True)
            coords_m = np.array(coordinates, copy=True)
            coords_p[atom_idx, axis_idx] += step
            coords_m[atom_idx, axis_idx] -= step

            driver.compute(_make_molecule_like(template_molecule, coords_p))
            e_p = float(driver.get_energy())
            driver.compute(_make_molecule_like(template_molecule, coords_m))
            e_m = float(driver.get_energy())

            fd[atom_idx, axis_idx] = (e_p - e_m) / (2.0 * step)
    return fd



def _evaluate_raw_weight_terms(driver, datapoint, weight_mode):
    if weight_mode == "cartesian":
        return driver.cartesian_distance(datapoint)
    if weight_mode == "internal":
        return driver.internal_distance(datapoint)
    raise ValueError(f"Unknown weight_mode: {weight_mode}")


def _finite_difference_raw_weight_gradient_mode(driver,
                                                datapoint,
                                                coordinates,
                                                step,
                                                weight_mode):
    fd = np.zeros_like(coordinates, dtype=np.float64)
    exclude = driver.symmetry_information[4]
    for atom_idx in range(coordinates.shape[0]):
        if atom_idx in exclude:
            continue
        for axis_idx in range(3):
            coords_p = np.array(coordinates, copy=True)
            coords_m = np.array(coordinates, copy=True)
            coords_p[atom_idx, axis_idx] += step
            coords_m[atom_idx, axis_idx] -= step

            driver.define_impes_coordinate(coords_p)
            _, _, denom_p, _, _, _, _, _ = _evaluate_raw_weight_terms(
                driver, datapoint, weight_mode)
            driver.define_impes_coordinate(coords_m)
            _, _, denom_m, _, _, _, _, _ = _evaluate_raw_weight_terms(
                driver, datapoint, weight_mode)

            fd[atom_idx, axis_idx] = (1.0 / denom_p - 1.0 / denom_m) / (2.0 * step)

    driver.define_impes_coordinate(coordinates)
    return fd


def _settings_with_bond_mode(settings, bond_mode):
    """
    Configure which bond coordinate map is active.

    Parameters
    ----------
    settings
        Base interpolation settings dictionary.
    bond_mode
        One of:
        - "inverse":     q_bond = 1 / r
        - "equilibrium": switched q_bond(r) between local r and 1 / r
        - "plain_r":     q_bond = r
    """
    patched = dict(settings)
    if bond_mode == "inverse":
        patched["use_inverse_bond_length"] = True
        patched["use_eq_bond_length"] = False
    elif bond_mode == "equilibrium":
        patched["use_inverse_bond_length"] = False
        patched["use_eq_bond_length"] = True
    elif bond_mode == "plain_r":
        patched["use_inverse_bond_length"] = False
        patched["use_eq_bond_length"] = False
    else:
        raise ValueError(f"Unsupported bond_mode={bond_mode}")
    return patched


def _build_fd_validation_datapoint(base_molecule,
                                   z_matrix,
                                   settings,
                                   bond_mode,
                                   mass_weighted):
    """
    Build a deterministic datapoint object used only for derivative checks.

    Why this helper exists:
    - We want `B` and `B2` checks that are independent of interpolation weights.
    - We want to test both unweighted Cartesian and mass-weighted Cartesian paths.
    - We want to test the currently-supported bond coordinate maps.
    """
    local_settings = _settings_with_bond_mode(settings, bond_mode)

    dp = InterpolationDatapoint(z_matrix)
    dp.update_settings(local_settings)
    dp.cartesian_coordinates = np.array(
        base_molecule.get_coordinates_in_bohr(), dtype=np.float64, copy=True)
    dp.define_internal_coordinates()

    if mass_weighted:
        inv_sqrt_masses = 1.0 / np.sqrt(np.repeat(base_molecule.get_masses(), 3))
        dp.inv_sqrt_masses = inv_sqrt_masses

    # The equilibrium-bond-length map needs reference bond lengths.
    # Use the current geometry so the transform is well-defined.
    if bond_mode == "equilibrium":
        coords = dp.cartesian_coordinates.reshape(-1)
        eq_bonds = []
        for idx, z_entry in enumerate(dp.z_matrix):
            if len(z_entry) == 2:
                eq_bonds.append(dp.internal_coordinates[idx].value(coords))
        dp.eq_bond_lengths = np.asarray(eq_bonds, dtype=np.float64)

    _debug_print(
        "Built FD datapoint: "
        f"bond_mode={bond_mode}, mass_weighted={mass_weighted}, "
        f"n_atoms={dp.cartesian_coordinates.shape[0]}, "
        f"n_internal={len(dp.z_matrix)}, "
        f"n_bonds={len(z_matrix['bonds'])}, n_angles={len(z_matrix['angles'])}, "
        f"n_dihedrals={len(z_matrix['dihedrals'])}, n_impropers={len(z_matrix['impropers'])}"
    )

    return dp


def _xprime_to_cartesian_flat(dp, xprime):
    """
    Convert finite-difference coordinates to physical Cartesian coordinates.

    Conventions:
    - If not mass-weighted: x' == x.
    - If mass-weighted in the implementation: B stores derivatives w.r.t.
      x' = sqrt(m) * x, therefore x = x' * (1/sqrt(m)).
    """
    xprime = np.asarray(xprime, dtype=np.float64).reshape(-1)
    if dp.inv_sqrt_masses is None:
        return xprime.copy()
    return xprime * dp.inv_sqrt_masses


def _evaluate_internal_values_at_xprime(dp, xprime):
    """
    Evaluate transformed internal-coordinate values at a probe point.

    This intentionally mirrors the production coordinate-map semantics used by
    InterpolationDatapoint, but does not reuse `B`/`B2` directly. The FD tests
    compare derivatives of this scalar-vector function against analytical
    `calculate_b_matrix()` and `calculate_b2_matrix()`.
    """
    x_cart = _xprime_to_cartesian_flat(dp, xprime)
    base_values = np.fromiter(
        (q.value(x_cart) for q in dp.internal_coordinates),
        dtype=np.float64,
        count=len(dp.internal_coordinates),
    )

    values = base_values.copy()
    row_cache = dp._get_b_matrix_row_cache()

    bond_rows = row_cache["bond_rows"]
    if bond_rows.size > 0:
        if dp.use_inverse_bond_length:
            values[bond_rows] = 1.0 / base_values[bond_rows]
        elif dp.use_eq_bond_length:
            eq_values = dp._get_eq_bond_lengths_array(bond_rows.size)
            q_values, _, _ = dp._switched_bond_transform(
                base_values[bond_rows],
                eq_values,
                eps_inner=0.205,
                eps_outer=0.215,
            )
            values[bond_rows] = q_values

    if dp.use_cosine_dihedral:
        dihedral_rows = row_cache["dihedral_rows"]
        if dihedral_rows.size > 0:
            dihedral_values = base_values[dihedral_rows]
            first_mask = row_cache["dihedral_first"]
            first_rows = dihedral_rows[first_mask]
            second_rows = dihedral_rows[~first_mask]
            if first_rows.size > 0:
                values[first_rows] = np.cos(dihedral_values[first_mask])
            if second_rows.size > 0:
                values[second_rows] = np.sin(dihedral_values[~first_mask])

    return values


def _finite_difference_b_matrix(dp, step=2.0e-6):
    """
    Central finite-difference Jacobian of transformed internal coordinates.

    Returns matrix shape (n_internal, n_cart), directly comparable to B.
    """
    x_cart = np.asarray(dp.cartesian_coordinates, dtype=np.float64).reshape(-1)
    if dp.inv_sqrt_masses is None:
        xprime0 = x_cart.copy()
    else:
        xprime0 = x_cart / dp.inv_sqrt_masses

    n_int = len(dp.z_matrix)
    n_cart = x_cart.size
    fd = np.zeros((n_int, n_cart), dtype=np.float64)

    periodic_rows = _periodic_internal_rows(dp)
    q0 = _evaluate_internal_values_at_xprime(dp, xprime0)

    for a in range(n_cart):
        e = np.zeros(n_cart, dtype=np.float64)
        e[a] = 1.0

        q_plus = _evaluate_internal_values_at_xprime(dp, xprime0 + step * e)
        q_minus = _evaluate_internal_values_at_xprime(dp, xprime0 - step * e)

        dq = q_plus - q_minus

        # Fix branch-cut artifacts for periodic torsions/impropers.
        if periodic_rows.size > 0:
            d_plus = _wrap_to_pi(q_plus[periodic_rows] - q0[periodic_rows])
            d_minus = _wrap_to_pi(q_minus[periodic_rows] - q0[periodic_rows])
            dq[periodic_rows] = d_plus - d_minus

        fd[:, a] = dq / (2.0 * step)

    return fd


def _finite_difference_b2_matrix(dp, step=1.0e-5):
    """
    Central finite-difference Hessian tensor of transformed internal coordinates.

    Returns tensor shape (n_internal, n_cart, n_cart), directly comparable to B2.
    """
    x_cart = np.asarray(dp.cartesian_coordinates, dtype=np.float64).reshape(-1)
    if dp.inv_sqrt_masses is None:
        xprime0 = x_cart.copy()
    else:
        xprime0 = x_cart / dp.inv_sqrt_masses

    n_int = len(dp.z_matrix)
    n_cart = x_cart.size
    fd2 = np.zeros((n_int, n_cart, n_cart), dtype=np.float64)

    q0 = _evaluate_internal_values_at_xprime(dp, xprime0)
    periodic_rows = _periodic_internal_rows(dp)

    for a in range(n_cart):
        ea = np.zeros(n_cart, dtype=np.float64)
        ea[a] = 1.0

        q_plus = _evaluate_internal_values_at_xprime(dp, xprime0 + step * ea)
        q_minus = _evaluate_internal_values_at_xprime(dp, xprime0 - step * ea)

        diag = (q_plus - 2.0 * q0 + q_minus) / (step**2)

        # Fix branch-cut artifacts on diagonal second derivatives.
        if periodic_rows.size > 0:
            d_plus = _wrap_to_pi(q_plus[periodic_rows] - q0[periodic_rows])
            d_minus = _wrap_to_pi(q_minus[periodic_rows] - q0[periodic_rows])
            diag[periodic_rows] = (d_plus + d_minus) / (step**2)

        fd2[:, a, a] = diag

        for b in range(a + 1, n_cart):
            eb = np.zeros(n_cart, dtype=np.float64)
            eb[b] = 1.0

            q_pp = _evaluate_internal_values_at_xprime(dp, xprime0 + step * ea + step * eb)
            q_pm = _evaluate_internal_values_at_xprime(dp, xprime0 + step * ea - step * eb)
            q_mp = _evaluate_internal_values_at_xprime(dp, xprime0 - step * ea + step * eb)
            q_mm = _evaluate_internal_values_at_xprime(dp, xprime0 - step * ea - step * eb)

            mixed = (q_pp - q_pm - q_mp + q_mm) / (4.0 * step**2)

            # Fix branch-cut artifacts on mixed second derivatives.
            if periodic_rows.size > 0:
                d_pp = _wrap_to_pi(q_pp[periodic_rows] - q0[periodic_rows])
                d_pm = _wrap_to_pi(q_pm[periodic_rows] - q0[periodic_rows])
                d_mp = _wrap_to_pi(q_mp[periodic_rows] - q0[periodic_rows])
                d_mm = _wrap_to_pi(q_mm[periodic_rows] - q0[periodic_rows])
                mixed[periodic_rows] = (d_pp - d_pm - d_mp + d_mm) / (4.0 * step**2)

            fd2[:, a, b] = mixed
            fd2[:, b, a] = mixed

    return fd2


@pytest.mark.parametrize("bond_mode", ["inverse", "equilibrium", "plain_r"])
@pytest.mark.parametrize("mass_weighted", [False, True])
def test_interpolationdatapoint_b_matrix_matches_finite_difference(request,
                                                                   bond_mode,
                                                                   mass_weighted):
    """
    Validate Wilson B matrix against a numerical Jacobian.

    What this checks:
    - Analytical B from `calculate_b_matrix()`.
    - Numerical B from central finite differences of transformed internals.

    Why this matters:
    - Any change in bond-coordinate transforms (r, 1/r, eq-map) must keep
      gradient chain-rule behavior correct.
    - The mass-weighted path is checked explicitly because B can be built in
      either coordinate convention.
    """
    base_mol, z_matrix, settings, _ = _base_molecule_system()
    dp = _build_fd_validation_datapoint(
        base_mol,
        z_matrix,
        settings,
        bond_mode=bond_mode,
        mass_weighted=mass_weighted,
    )
    dp.calculate_b_matrix()
    fd_step = 2.0e-6
    b_fd = _finite_difference_b_matrix(dp, step=fd_step)

    abs_err = np.max(np.abs(dp.b_matrix - b_fd))
    rel_err = np.linalg.norm(dp.b_matrix - b_fd) / (np.linalg.norm(b_fd) + 1.0e-12)
    threshold_abs = 2.0e-5
    threshold_rel = 2.0e-5

    checks = [
        f"bond_mode={bond_mode}",
        f"mass_weighted={mass_weighted}",
        f"max_abs_error={abs_err:.6e}",
        f"relative_error={rel_err:.6e}",
        f"abs_threshold={threshold_abs:.6e}",
        f"rel_threshold={threshold_rel:.6e}",
        f"status={'PASS' if (abs_err < threshold_abs and rel_err < threshold_rel) else 'FAIL'}",
    ]
    if _detailed_arrays_enabled():
        checks.extend(_array_comparison_lines("B analytical vs FD", dp.b_matrix, b_fd))

    _emit_report(
        request,
        "InterpolationDatapoint B-Matrix FD Validation",
        ("Analytical Wilson B matrix must match finite-difference Jacobian of "
         "the transformed internal-coordinate values."),
        checks,
    )

    if _debug_enabled() or abs_err >= threshold_abs or rel_err >= threshold_rel:
        delta = dp.b_matrix - b_fd
        flat_idx = int(np.argmax(np.abs(delta)))
        row, col = np.unravel_index(flat_idx, delta.shape)
        _debug_print(
            "B check summary: "
            f"bond_mode={bond_mode}, mass_weighted={mass_weighted}, "
            f"shape={delta.shape}, max_abs={abs_err:.6e}, rel={rel_err:.6e}"
        )
        _debug_print(
            "B argmax element: "
            f"row={row}, col={col}, z_entry={dp.z_matrix[row]}, "
            f"analytical={dp.b_matrix[row, col]:.12e}, "
            f"fd={b_fd[row, col]:.12e}, "
            f"delta={delta[row, col]:.12e}"
        )
        for rank, idx in enumerate(_topk_abs_indices(delta, k=5), start=1):
            i_row, i_col = np.unravel_index(idx, delta.shape)
            _debug_print(
                f"B top{rank}: row={i_row}, col={i_col}, z_entry={dp.z_matrix[i_row]}, "
                f"analytical={dp.b_matrix[i_row, i_col]:.12e}, "
                f"fd={b_fd[i_row, i_col]:.12e}, "
                f"delta={delta[i_row, i_col]:.12e}"
            )
        x_cart = np.asarray(dp.cartesian_coordinates, dtype=np.float64).reshape(-1)
        if dp.inv_sqrt_masses is None:
            xprime0 = x_cart.copy()
        else:
            xprime0 = x_cart / dp.inv_sqrt_masses
        e = np.zeros_like(xprime0)
        e[col] = 1.0
        fd_recomputed = None
        q0 = _evaluate_internal_values_at_xprime(dp, xprime0)[row]
        q_plus = _evaluate_internal_values_at_xprime(dp, xprime0 + fd_step * e)[row]
        q_minus = _evaluate_internal_values_at_xprime(dp, xprime0 - fd_step * e)[row]
        is_periodic = (len(dp.z_matrix[row]) == 4) and (not dp.use_cosine_dihedral)

        if is_periodic:
            d_plus = _wrap_to_pi(q_plus - q0)
            d_minus = _wrap_to_pi(q_minus - q0)
            fd_recomputed = (d_plus - d_minus) / (2.0 * fd_step)
        else:
            fd_recomputed = (q_plus - q_minus) / (2.0 * fd_step)

        _debug_print(
            "B stencil at argmax: "
            f"row={row}, col={col}, "
            f"q_minus={q_minus:.12e}, q0={q0:.12e}, q_plus={q_plus:.12e}, "
            f"fd_recomputed={fd_recomputed:.12e}"
        )

    assert abs_err < threshold_abs
    assert rel_err < threshold_rel


