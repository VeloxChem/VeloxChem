def _topk_abs_indices(arr, k=5):
    flat = np.abs(np.asarray(arr)).reshape(-1)
    if flat.size == 0:
        return []
    k_eff = int(min(max(k, 1), flat.size))
    idx = np.argpartition(flat, -k_eff)[-k_eff:]
    idx = idx[np.argsort(flat[idx])[::-1]]
    return idx.tolist()


