@pytest.mark.parametrize("variant_label,use_symmetry", RUN_VARIANTS)
@pytest.mark.parametrize(
    "weight_mode,seed,threshold",
    [
        ("cartesian", 4, 1.0e-4),
    ],
)
def test_alphaoptimizer_jacobian_matches_finite_difference(
    monkeypatch,
    request,
    variant_label,
    use_symmetry,
    weight_mode,
    seed,
    threshold,
):
    monkeypatch.setattr(alphaoptimizer_module, "ProcessPoolExecutor", _SerialProcessPool)

    (
        base_mol,
        z_matrix,
        settings,
        sym_info,
        _bank,
        datapoints,
        sym_bank,
        _driver,
        probe_coords,
    ) = _build_reference_case(variant_label, use_symmetry, weight_mode=weight_mode, seed=1600 + seed)

    structures = [Molecule.from_xyz_string(string) for string in REF_STRUCTURES_ALPHA]

    ref_driver = _build_driver(z_matrix, settings, sym_info, datapoints)
    ref_driver.qm_symmetry_data_points = copy.deepcopy(sym_bank)
    ref_driver.mark_runtime_data_cache_dirty()

    qm_energies = REF_ENERGIES_ALPHA
    qm_gradients = [np.array(grad) for grad in REF_GRADIENTS_ALPHA]

    optimizer = alphaoptimizer_module.AlphaOptimizer(
        z_matrix,
        settings,
        sym_info,
        sym_bank,
        datapoints,
        structures,
        qm_energies,
        qm_gradients,
        exponent_p_q=(2, 2),
        e_x=0.5,
        beta=0.8,
        n_workers=1,
        verbose=False,
    )

    alphas = np.array([dp.confidence_radius for dp in datapoints], dtype=np.float64)
    _ = optimizer.fun(alphas)
    analytic_jac = np.array(optimizer.jac(alphas), copy=True)

    fd_jac = np.zeros_like(alphas)
    step = 1.0e-4
    for i in range(alphas.shape[0]):
        a_plus = np.array(alphas, copy=True)
        a_minus = np.array(alphas, copy=True)
        a_plus[i] += step
        a_minus[i] -= step
        fd_jac[i] = (optimizer.fun(a_plus) - optimizer.fun(a_minus)) / (2.0 * step)
    rel_err = np.linalg.norm(analytic_jac - fd_jac) / (np.linalg.norm(fd_jac) + 1.0e-12)

    if _fd_dump_enabled() or _debug_enabled() or rel_err >= threshold:
        _print_fd_comparison(
            f"Alpha-optimizer Jacobian FD comparison ({variant_label}, {weight_mode})",
            analytic_jac,
            fd_jac,
        )

    optimizer.close()

    checks = [
        f"variant={variant_label}",
        f"weight_mode={weight_mode}",
        f"seed={seed}",
        f"n_datapoints={len(datapoints)}",
        f"n_alphas={alphas.size}",
        f"step={step:.1e}",
        f"relative_error={rel_err:.6e}",
        f"threshold={threshold:.6e}",
        f"status={'PASS' if rel_err < threshold else 'FAIL'}",
    ]
    if _detailed_arrays_enabled():
        checks.extend(_array_comparison_lines("jacobian comparison", analytic_jac, fd_jac))

    _emit_report(
        request,
        "AlphaOptimizer Jacobian",
        ("Analytic Jacobian from optimizer must match finite-difference Jacobian "
         f"for variant={variant_label}, mode={weight_mode}, use_tc_weights=False."),
        checks,
    )

    assert rel_err < threshold

