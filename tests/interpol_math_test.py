    raw_weight_grads = np.asarray(raw_weight_grads, dtype=np.float64)

    sum_w = raw_weights.sum()
    sum_grad_w = raw_weight_grads.sum(axis=0)
    normalized_w = raw_weights / sum_w
    normalized_grad_w = (
        raw_weight_grads * sum_w - raw_weights[:, None, None] * sum_grad_w
    ) / (sum_w ** 2)

    potentials = np.asarray(driver.potentials, dtype=np.float64)
    gradients = np.asarray(driver.gradients, dtype=np.float64)

    energy_reconstructed = float(np.dot(normalized_w, potentials))
    gradient_reconstructed = (
        np.tensordot(normalized_w, gradients, axes=1)
        + np.tensordot(potentials, normalized_grad_w, axes=1)
    )

    sum_w_close = np.isclose(normalized_w.sum(), 1.0, atol=1.0e-12, rtol=1.0e-12)
    energy_close = np.isclose(
        energy_reconstructed, driver.get_energy(), atol=1.0e-12, rtol=1.0e-12
    )
    grad_close = np.allclose(
        gradient_reconstructed, driver.get_gradient(), atol=1.0e-12, rtol=1.0e-12
    )

    _emit_report(
        request,
        "Normalized Weight Identities",
        ("Validate algebraic consistency: sum(W)=1, E=sum(WU), "
         "and grad(E)=sum(W*grad(U))+sum(U*grad(W))."),
        checks=[
            f"variant={variant_label}",
            f"sumW={normalized_w.sum():.12f} -> {'PASS' if sum_w_close else 'FAIL'}",
            f"|E_reconstructed-E_model|={abs(energy_reconstructed - driver.get_energy()):.6e} -> "
            f"{'PASS' if energy_close else 'FAIL'}",
            f"||G_reconstructed-G_model||={np.linalg.norm(gradient_reconstructed - driver.get_gradient()):.6e} -> "
            f"{'PASS' if grad_close else 'FAIL'}",
            f"normalized_weights={_array_str(normalized_w)}",
        ] + (
            _array_comparison_lines(
                "reconstructed-vs-driver gradient",
                gradient_reconstructed,
                driver.get_gradient(),
            ) if _detailed_arrays_enabled() else []
        ),
    )

    assert sum_w_close
    assert energy_close
    assert grad_close

