def _wrap_to_pi(delta):
    """
    Map angular differences to (-pi, pi].
    Works on scalars and numpy arrays.
    """
    return (np.asarray(delta, dtype=np.float64) + np.pi) % (2.0 * np.pi) - np.pi


def _periodic_internal_rows(dp):
    """
    Return rows in dp.z_matrix that are periodic angles (dihedrals/impropers).
    If cosine-dihedral mode is active, transformed coordinates are non-periodic
    (sin/cos components), so no wrapping is applied.
    """
    if getattr(dp, "use_cosine_dihedral", False):
        return np.empty(0, dtype=np.int64)

    rows = [i for i, z_entry in enumerate(dp.z_matrix) if len(z_entry) == 4]
    return np.asarray(rows, dtype=np.int64)

def _validate_manual_masks_for_zmatrix(z_matrix):
    n_internal = (
        len(z_matrix["bonds"])
        + len(z_matrix["angles"])
        + len(z_matrix["dihedrals"])
        + len(z_matrix["impropers"])
    )
    masks = np.asarray(MANUAL_CORE_PLUS_ONE_SYM_MASKS, dtype=np.int64)

    if masks.ndim != 2:
        raise AssertionError(f"MANUAL_CORE_PLUS_ONE_SYM_MASKS must be 2D, got {masks.ndim}D")
    if masks.shape[1] != n_internal:
        raise AssertionError(
            "Mask width must match total number of internal coordinates "
            f"(got {masks.shape[1]} vs {n_internal})."
        )

    expected = np.arange(n_internal, dtype=np.int64)
    for ridx, row in enumerate(masks):
        if not np.array_equal(np.sort(row), expected):
            raise AssertionError(
                f"Mask row {ridx} is not a permutation of 0..{n_internal - 1}."
            )

    return np.array(masks, copy=True)

def _coords_with_dihedral_shift(template_mol, dihedral, delta_rad):
    """
    Build coordinates with one proper-dihedral rotated by delta_rad.
    dihedral is 0-based tuple, Molecule API expects 1-based indices.
    """
    mol = Molecule(
        template_mol.get_labels(),
        template_mol.get_coordinates_in_bohr().copy(),
        "bohr",
    )
    d = tuple(int(i) + 1 for i in dihedral)
    phi0 = mol.get_dihedral(d, "radian")
    mol.set_dihedral(d, phi0 + float(delta_rad), "radian")
    return mol.get_coordinates_in_bohr().copy()

