def test_symmetry_adaptation_candidate_order_invariant(request):
    (
        base_mol,
        z_matrix,
        settings,
        sym_info,
        bank,
        datapoints,
        _sym_bank,
        _driver,
        probe_coords,
    ) = _build_reference_case("symmetry", True, seed=1501)

    pair_a = [bank["point_1"], bank["point_1_symmetry"]]
    pair_b = [bank["point_1_symmetry"], bank["point_1"]]
    pair_2 = [bank["point_2"], bank["point_2_symmetry"]]

    def _run_with_candidate_order(pair_for_point_1):
        drv = _build_driver(z_matrix, settings, sym_info, datapoints)
        drv.qm_symmetry_data_points = {
            "point_1": pair_for_point_1,
            "point_1_symmetry": pair_for_point_1,
            "point_2": pair_2,
            "point_2_symmetry": pair_2,
        }
        drv.mark_runtime_data_cache_dirty()
        drv.compute(_make_molecule_like(base_mol, probe_coords))
        return float(drv.get_energy()), np.array(drv.get_gradient(), copy=True)

    energy_a, grad_a = _run_with_candidate_order(pair_a)
    energy_b, grad_b = _run_with_candidate_order(pair_b)

    energy_close = np.isclose(energy_a, energy_b, atol=1.0e-12, rtol=1.0e-12)
    grad_close = np.allclose(grad_a, grad_b, atol=1.0e-12, rtol=1.0e-12)

    _emit_report(
        request,
        "Symmetry Candidate Ordering Invariance",
        ("Changing ordering of symmetry candidates should not change final "
         "interpolated energy/gradient."),
        checks=[
            f"|E(orderA)-E(orderB)|={abs(energy_a - energy_b):.6e} -> {'PASS' if energy_close else 'FAIL'}",
            f"||G(orderA)-G(orderB)||={np.linalg.norm(grad_a - grad_b):.6e} -> {'PASS' if grad_close else 'FAIL'}",
        ] + (
            _array_comparison_lines("gradient(orderA)-gradient(orderB)", grad_a, grad_b)
            if _detailed_arrays_enabled() else []
        ),
    )

    assert energy_close
    assert grad_close

