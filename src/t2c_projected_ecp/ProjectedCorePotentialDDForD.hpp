                    t2pecp::comp_prim_projected_core_potential_sd_p(pbuffer, 72, 64, 66, 108, 126, 131, 0, -1, -1, pfactors, 2, r_a, a_exp, c_exp);

                    t2pecp::comp_prim_projected_core_potential_sd_p(pbuffer, 189, 172, 177, 215, 223, 228, 1, 106, 111, pfactors, 2, r_a, a_exp, c_exp);

                    t2pecp::comp_prim_projected_core_potential_sd_d(pbuffer, 4, 0, 1, 66, 121, 122, 125, 128, 0, -1, -1, pfactors, 2, r_a, a_exp, c_exp);

