                    t2pecp::comp_prim_projected_core_potential_pp_p(pbuffer, 78, 66, 105, 108, 177, 0, -1, pfactors, 2, r_a, a_exp, c_exp);

                    t2pecp::comp_prim_projected_core_potential_pd_s(pbuffer, 195, 183, 253, 0, -1, pfactors, r_a, a_exp, c_exp);

                    t2pecp::comp_prim_projected_core_potential_pd_p(pbuffer, 87, 72, 108, 114, 189, 0, -1, pfactors, 2, r_a, a_exp, c_exp);

