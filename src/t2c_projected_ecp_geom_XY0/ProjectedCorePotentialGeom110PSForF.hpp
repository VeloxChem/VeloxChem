                    t2pecp::comp_prim_projected_core_potential_ss(0, 2, 2, 1, pbuffer, 226, i_values, l_values,  pfactors, 7, 5, r_a, a_norm, c_norm);

                    t2pecp::comp_prim_projected_core_potential_ss(3, 3, 0, 0, pbuffer, 227, i_values, l_values,  pfactors, 7, 5, r_a, a_norm, c_norm);

                    t2pecp::comp_prim_projected_core_potential_ss(1, 3, 0, 1, pbuffer, 228, i_values, l_values,  pfactors, 7, 5, r_a, a_norm, c_norm);

