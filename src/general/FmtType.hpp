#ifndef FmtType_hpp
#define FmtType_hpp

/**
 Enumerate class fmt_t:

 Defines supported formatting keys:
 fmt::left   - the left alignment of output line
 fmt::center - the center alignment of output line
 fmt::right  - the right alignment of output line
 */
enum class fmt_t
{
    left,
    center,
    right
};

#endif /* FmtType_hpp */
