                    t2pecp::comp_prim_projected_core_potential_sp_s(pbuffer, 1, 0, 346, 0, -1, pfactors, 2, a_exp, c_exp);

                    t2pecp::comp_prim_projected_core_potential_sp_s(pbuffer, 342, 341, 350, 0, -1, pfactors, 2, a_exp, c_exp);

