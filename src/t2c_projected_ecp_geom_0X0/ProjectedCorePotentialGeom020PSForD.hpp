                    t2pecp::comp_prim_projected_core_potential_ss(1, 0, 1, 1, pbuffer, 137, i_values, l_values,  pfactors, 7, 5, r_a, a_norm, c_norm);

                    t2pecp::comp_prim_projected_core_potential_ss(2, 0, 1, 1, pbuffer, 138, i_values, l_values,  pfactors, 7, 5, r_a, a_norm, c_norm);

